static uint8_t g_ng_m68k_cycle_table[0x10000u];
static int g_ng_m68k_cycle_table_ready;

static void ng_m68k_init_cycle_table(void) {
    if (g_ng_m68k_cycle_table_ready) {
        return;
    }

    memset(g_ng_m68k_cycle_table, 0, sizeof(g_ng_m68k_cycle_table));
    for (uint32_t i = 0;
         i < (uint32_t)(sizeof(k_ng_m68k_cycle_entries) /
                        sizeof(k_ng_m68k_cycle_entries[0]));
         ++i) {
        const NgM68kCycleEntry *entry = &k_ng_m68k_cycle_entries[i];
        uint32_t extra = 0;
        do {
            uint16_t opcode = (uint16_t)(entry->match | extra);
            g_ng_m68k_cycle_table[opcode] = entry->cycles;
            extra = ((extra | entry->mask) + 1u) & (uint32_t)~entry->mask;
            extra &= 0xFFFFu;
        } while (extra != 0u);
    }
    g_ng_m68k_cycle_table_ready = 1;
}

uint8_t ng_m68k_base_cycles_for_opcode(uint16_t opcode) {
    ng_m68k_init_cycle_table();
    uint8_t cycles = g_ng_m68k_cycle_table[opcode];
    return cycles != 0u ? cycles : 4u;
}

uint8_t ng_m68k_base_cycles_for_instr(const NgM68kInstr *instr) {
    if (!instr) {
        return 4u;
    }
    return ng_m68k_base_cycles_for_opcode(instr->opcode);
}
