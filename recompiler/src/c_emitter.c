#define NG_EMIT_MAX_INSTRUCTIONS 192u
#define NG_CCR_C 0x0001u
#define NG_CCR_V 0x0002u
#define NG_CCR_Z 0x0004u
#define NG_CCR_N 0x0008u
#define NG_CCR_X 0x0010u
#define NG_SR_S 0x2000u

void ng_c_symbol_for_addr(uint32_t addr, char *out, unsigned out_size) {
    snprintf(out, out_size, "ng_func_%06X", addr & 0xFFFFFFu);
}

void ng_emit_diagnostics_init(NgEmitDiagnostics *diagnostics) {
    if (diagnostics) {
        memset(diagnostics, 0, sizeof(*diagnostics));
        diagnostics->first_unsupported_addr = 0xFFFFFFFFu;
        diagnostics->first_decode_error_addr = 0xFFFFFFFFu;
    }
}

static void emit_record_unsupported(NgEmitDiagnostics *diagnostics, uint32_t addr) {
    if (!diagnostics) {
        return;
    }
    if (diagnostics->unsupported_count == 0u) {
        diagnostics->first_unsupported_addr = addr & 0x00FFFFFFu;
    }
    ++diagnostics->unsupported_count;
}

static void emit_record_decode_error(NgEmitDiagnostics *diagnostics, uint32_t addr) {
    if (!diagnostics) {
        return;
    }
    if (diagnostics->decode_error_count == 0u) {
        diagnostics->first_decode_error_addr = addr & 0x00FFFFFFu;
    }
    ++diagnostics->decode_error_count;
}

static void ng_c_label_for_addr(uint32_t addr, char *out, unsigned out_size) {
    snprintf(out, out_size, "ng_label_%06X", addr & 0xFFFFFFu);
}

static void emit_header(FILE *out) {
    fprintf(out, "/* Generated by neo-recomp. */\n");
    fprintf(out, "#include <stdint.h>\n");
    fprintf(out, "#include \"ngrecomp/neogeo_runtime.h\"\n\n");
    fprintf(out, "#define NG_CCR_C 0x0001u\n");
    fprintf(out, "#define NG_CCR_V 0x0002u\n");
    fprintf(out, "#define NG_CCR_Z 0x0004u\n");
    fprintf(out, "#define NG_CCR_N 0x0008u\n\n");
    fprintf(out, "#define NG_CCR_X 0x0010u\n\n");
    fprintf(out, "#define NG_SR_S 0x2000u\n\n");
    fprintf(out, "void ng_generated_call(uint32_t addr);\n\n");
    fprintf(out, "static void ng_set_sr(uint16_t value) {\n");
    fprintf(out, "    uint16_t old_sr = g_ng_m68k.sr;\n");
    fprintf(out, "    if ((old_sr ^ value) & NG_SR_S) {\n");
    fprintf(out, "        if (old_sr & NG_SR_S) {\n");
    fprintf(out, "            g_ng_m68k.ssp = g_ng_m68k.a[7];\n");
    fprintf(out, "        } else {\n");
    fprintf(out, "            g_ng_m68k.usp = g_ng_m68k.a[7];\n");
    fprintf(out, "        }\n");
    fprintf(out, "        g_ng_m68k.a[7] = (value & NG_SR_S) ? g_ng_m68k.ssp : g_ng_m68k.usp;\n");
    fprintf(out, "    }\n");
    fprintf(out, "    g_ng_m68k.sr = value;\n");
    fprintf(out, "}\n\n");
    fprintf(out, "static void ng_push_exception_frame(uint32_t return_pc) {\n");
    fprintf(out, "    uint16_t saved_sr = g_ng_m68k.sr;\n");
    fprintf(out, "    ng_set_sr((uint16_t)(saved_sr | NG_SR_S));\n");
    fprintf(out, "    g_ng_m68k.a[7] -= 4u;\n");
    fprintf(out, "    ng68k_write32(g_ng_m68k.a[7], return_pc & 0x00FFFFFFu);\n");
    fprintf(out, "    g_ng_m68k.a[7] -= 2u;\n");
    fprintf(out, "    ng68k_write16(g_ng_m68k.a[7], saved_sr);\n");
    fprintf(out, "}\n\n");
    fprintf(out, "static int ng_require_supervisor(uint32_t fault_pc) {\n");
    fprintf(out, "    if (g_ng_m68k.sr & NG_SR_S) return 1;\n");
    fprintf(out, "    ng_push_exception_frame(fault_pc);\n");
    fprintf(out, "    ng_generated_call(ng68k_read32(0x00000020u));\n");
    fprintf(out, "    return 0;\n");
    fprintf(out, "}\n\n");
    fprintf(out, "static void ng_push_interrupt_frame(uint32_t return_pc, uint8_t level) {\n");
    fprintf(out, "    uint16_t saved_sr = g_ng_m68k.sr;\n");
    fprintf(out, "    uint16_t next_sr = (uint16_t)((saved_sr | NG_SR_S) & 0xF8FFu);\n");
    fprintf(out, "    next_sr = (uint16_t)(next_sr | (uint16_t)((level & 7u) << 8));\n");
    fprintf(out, "    ng_set_sr(next_sr);\n");
    fprintf(out, "    g_ng_m68k.a[7] -= 4u;\n");
    fprintf(out, "    ng68k_write32(g_ng_m68k.a[7], return_pc & 0x00FFFFFFu);\n");
    fprintf(out, "    g_ng_m68k.a[7] -= 2u;\n");
    fprintf(out, "    ng68k_write16(g_ng_m68k.a[7], saved_sr);\n");
    fprintf(out, "}\n\n");
    fprintf(out, "static int ng_service_interrupt(uint32_t return_pc) {\n");
    fprintf(out, "    uint8_t level = 0;\n");
    fprintf(out, "    uint8_t vector = 0;\n");
    fprintf(out, "    uint8_t current_mask = (uint8_t)((g_ng_m68k.sr >> 8) & 7u);\n");
    fprintf(out, "    if (!ng_m68k_take_interrupt(current_mask, &level, &vector)) return 0;\n");
    fprintf(out, "    ng_push_interrupt_frame(return_pc, level);\n");
    fprintf(out, "    ng_generated_call(ng68k_read32((uint32_t)vector * 4u));\n");
    fprintf(out, "    return 1;\n");
    fprintf(out, "}\n\n");
    fprintf(out, "static void ng_set_nz16(uint16_t value) {\n");
    fprintf(out, "    g_ng_m68k.sr = (uint16_t)(g_ng_m68k.sr & 0xFFF0u);\n");
    fprintf(out, "    if (value == 0) g_ng_m68k.sr |= NG_CCR_Z;\n");
    fprintf(out, "    if (value & 0x8000u) g_ng_m68k.sr |= NG_CCR_N;\n");
    fprintf(out, "}\n\n");
    fprintf(out, "static void ng_set_nz8(uint8_t value) {\n");
    fprintf(out, "    g_ng_m68k.sr = (uint16_t)(g_ng_m68k.sr & 0xFFF0u);\n");
    fprintf(out, "    if (value == 0) g_ng_m68k.sr |= NG_CCR_Z;\n");
    fprintf(out, "    if (value & 0x80u) g_ng_m68k.sr |= NG_CCR_N;\n");
    fprintf(out, "}\n\n");
    fprintf(out, "static void ng_set_nz32(uint32_t value) {\n");
    fprintf(out, "    g_ng_m68k.sr = (uint16_t)(g_ng_m68k.sr & 0xFFF0u);\n");
    fprintf(out, "    if (value == 0) g_ng_m68k.sr |= NG_CCR_Z;\n");
    fprintf(out, "    if (value & 0x80000000u) g_ng_m68k.sr |= NG_CCR_N;\n");
    fprintf(out, "}\n\n");
}

static void emit_declarations(FILE *out, const NgFunctionDiscovery *discovery) {
    for (uint32_t i = 0; i < discovery->count; ++i) {
        char symbol[32];
        ng_c_symbol_for_addr(discovery->addrs[i], symbol, (unsigned)sizeof(symbol));
        fprintf(out, "static void %s(void);\n", symbol);
    }
}

static void emit_dispatch(FILE *out, const NgFunctionDiscovery *discovery) {
    fprintf(out, "\n");
    fprintf(out, "void ng_generated_call(uint32_t addr) {\n");
    fprintf(out, "    switch (addr & 0x00FFFFFFu) {\n");
    for (uint32_t i = 0; i < discovery->count; ++i) {
        char symbol[32];
        uint32_t addr = discovery->addrs[i] & 0xFFFFFFu;
        ng_c_symbol_for_addr(addr, symbol, (unsigned)sizeof(symbol));
        fprintf(out, "    case 0x%08Xu: %s(); return;\n", addr, symbol);
    }
    fprintf(out, "    default: ng_log_dispatch_miss(addr); return;\n");
    fprintf(out, "    }\n");
    fprintf(out, "}\n\n");
}

static const char *ng_condition_expr(uint8_t condition) {
    switch (condition) {
    case 2u:
        return "((g_ng_m68k.sr & (NG_CCR_C | NG_CCR_Z)) == 0)";
    case 3u:
        return "((g_ng_m68k.sr & (NG_CCR_C | NG_CCR_Z)) != 0)";
    case 4u:
        return "((g_ng_m68k.sr & NG_CCR_C) == 0)";
    case 5u:
        return "((g_ng_m68k.sr & NG_CCR_C) != 0)";
    case 6u:
        return "((g_ng_m68k.sr & NG_CCR_Z) == 0)";
    case 7u:
        return "((g_ng_m68k.sr & NG_CCR_Z) != 0)";
    case 8u:
        return "((g_ng_m68k.sr & NG_CCR_V) == 0)";
    case 9u:
        return "((g_ng_m68k.sr & NG_CCR_V) != 0)";
    case 10u:
        return "((g_ng_m68k.sr & NG_CCR_N) == 0)";
    case 11u:
        return "((g_ng_m68k.sr & NG_CCR_N) != 0)";
    case 12u:
        return "(((g_ng_m68k.sr & NG_CCR_N) != 0) == ((g_ng_m68k.sr & NG_CCR_V) != 0))";
    case 13u:
        return "(((g_ng_m68k.sr & NG_CCR_N) != 0) != ((g_ng_m68k.sr & NG_CCR_V) != 0))";
    case 14u:
        return "((g_ng_m68k.sr & NG_CCR_Z) == 0 && (((g_ng_m68k.sr & NG_CCR_N) != 0) == ((g_ng_m68k.sr & NG_CCR_V) != 0)))";
    case 15u:
        return "((g_ng_m68k.sr & NG_CCR_Z) != 0 || (((g_ng_m68k.sr & NG_CCR_N) != 0) != ((g_ng_m68k.sr & NG_CCR_V) != 0)))";
    default:
        return NULL;
    }
}

static int emit_branch_condition(FILE *out,
                                 const NgM68kInstr *instr,
                                 const char *target_label) {
    const char *expr = ng_condition_expr(instr->condition);

    if (instr->condition == 0u) {
        fprintf(out, "    goto %s;\n", target_label);
        return 1;
    }
    if (instr->condition == 1u) {
        fprintf(out, "    /* BF never branches. */\n");
        return 1;
    }

    if (expr) {
        fprintf(out, "    if (%s) goto %s;\n", expr, target_label);
        return 1;
    }

    fprintf(out, "    ng_log_dispatch_miss(0x%08Xu);\n", instr->addr & 0x00FFFFFFu);
    fprintf(out, "    return;\n");
    return 0;
}

static void emit_exception_vector_call(FILE *out,
                                       uint8_t vector,
                                       uint32_t return_pc) {
    fprintf(out, "    ng_push_exception_frame(0x%08Xu);\n",
            return_pc & 0x00FFFFFFu);
    fprintf(out, "    ng_generated_call(ng68k_read32(0x%08Xu));\n",
            (uint32_t)vector * 4u);
    fprintf(out, "    return;\n");
}

static void emit_push_return_address(FILE *out, uint32_t return_pc) {
    fprintf(out, "    g_ng_m68k.a[7] -= 4u;\n");
    fprintf(out, "    ng68k_write32(g_ng_m68k.a[7], 0x%08Xu);\n",
            return_pc & 0x00FFFFFFu);
}

static void emit_require_supervisor(FILE *out, uint32_t fault_pc) {
    fprintf(out, "    if (!ng_require_supervisor(0x%08Xu)) return;\n",
            fault_pc & 0x00FFFFFFu);
}

static const char *ng_read_fn_for_size(uint8_t size) {
    if (size == 4u) {
        return "ng68k_read32";
    }
    return size == 1u ? "ng68k_read8" : "ng68k_read16";
}

static const char *ng_write_fn_for_size(uint8_t size) {
    if (size == 4u) {
        return "ng68k_write32";
    }
    return size == 1u ? "ng68k_write8" : "ng68k_write16";
}

static const char *ng_ctype_for_size(uint8_t size) {
    if (size == 4u) {
        return "uint32_t";
    }
    return size == 1u ? "uint8_t" : "uint16_t";
}

static uint32_t ng_dreg_keep_mask(uint8_t size) {
    if (size == 4u) {
        return 0x00000000u;
    }
    return size == 1u ? 0xFFFFFF00u : 0xFFFF0000u;
}

static uint32_t ng_value_mask(uint8_t size) {
    if (size == 4u) {
        return 0xFFFFFFFFu;
    }
    return size == 1u ? 0x000000FFu : 0x0000FFFFu;
}

static uint8_t ng_ea_step_bytes(const NgM68kEa *ea, uint8_t size) {
    if (ea->reg == 7u && size == 1u) {
        return 2u;
    }
    return size;
}

static void ng_format_index_expr(const NgM68kEa *ea, char *out, unsigned out_size) {
    const char *regs = ea->index_is_addr ? "g_ng_m68k.a" : "g_ng_m68k.d";
    if (ea->index_is_long) {
        snprintf(out, out_size, "(int32_t)%s[%u]", regs, ea->index_reg);
    } else {
        snprintf(out, out_size, "(int32_t)(int16_t)(%s[%u] & 0xFFFFu)",
                 regs, ea->index_reg);
    }
}

static int emit_ea_read(FILE *out,
                        const NgM68kInstr *instr,
                        const NgM68kEa *ea,
                        uint8_t size,
                        char *expr,
                        unsigned expr_size) {
    char tmp[48];
    const char *read_fn = ng_read_fn_for_size(size);
    const char *ctype = ng_ctype_for_size(size);

    switch (ea->mode) {
    case NG_M68K_EA_DREG:
        if (size == 4u) {
            snprintf(expr, expr_size, "g_ng_m68k.d[%u]", ea->reg);
        } else {
            snprintf(expr, expr_size, "(%s)(g_ng_m68k.d[%u] & 0x%0*Xu)",
                     ctype, ea->reg, size == 1u ? 2 : 4, ng_value_mask(size));
        }
        return 1;
    case NG_M68K_EA_AREG:
        snprintf(expr, expr_size, "g_ng_m68k.a[%u]", ea->reg);
        return 1;
    case NG_M68K_EA_AIND:
        snprintf(tmp, sizeof(tmp), "ng_ea_%06X", instr->addr & 0x00FFFFFFu);
        fprintf(out, "    %s %s = %s(g_ng_m68k.a[%u]);\n",
                ctype, tmp, read_fn, ea->reg);
        snprintf(expr, expr_size, "%s", tmp);
        return 1;
    case NG_M68K_EA_APOST:
        snprintf(tmp, sizeof(tmp), "ng_ea_%06X", instr->addr & 0x00FFFFFFu);
        fprintf(out, "    %s %s = %s(g_ng_m68k.a[%u]);\n",
                ctype, tmp, read_fn, ea->reg);
        fprintf(out, "    g_ng_m68k.a[%u] += %uu;\n",
                ea->reg, (unsigned)ng_ea_step_bytes(ea, size));
        snprintf(expr, expr_size, "%s", tmp);
        return 1;
    case NG_M68K_EA_APRE:
        snprintf(tmp, sizeof(tmp), "ng_ea_%06X", instr->addr & 0x00FFFFFFu);
        fprintf(out, "    g_ng_m68k.a[%u] -= %uu;\n",
                ea->reg, (unsigned)ng_ea_step_bytes(ea, size));
        fprintf(out, "    %s %s = %s(g_ng_m68k.a[%u]);\n",
                ctype, tmp, read_fn, ea->reg);
        snprintf(expr, expr_size, "%s", tmp);
        return 1;
    case NG_M68K_EA_ADISP:
        snprintf(tmp, sizeof(tmp), "ng_ea_%06X", instr->addr & 0x00FFFFFFu);
        fprintf(out, "    %s %s = %s((uint32_t)(g_ng_m68k.a[%u] + (int32_t)%d));\n",
                ctype, tmp, read_fn, ea->reg, ea->displacement);
        snprintf(expr, expr_size, "%s", tmp);
        return 1;
    case NG_M68K_EA_ABS_W:
    case NG_M68K_EA_ABS_L:
        snprintf(tmp, sizeof(tmp), "ng_ea_%06X", instr->addr & 0x00FFFFFFu);
        fprintf(out, "    %s %s = %s(0x%08Xu);\n",
                ctype, tmp, read_fn, ea->absolute_addr & 0x00FFFFFFu);
        snprintf(expr, expr_size, "%s", tmp);
        return 1;
    case NG_M68K_EA_PC_DISP:
        snprintf(tmp, sizeof(tmp), "ng_ea_%06X", instr->addr & 0x00FFFFFFu);
        fprintf(out, "    %s %s = %s(0x%08Xu);\n",
                ctype, tmp, read_fn, ea->absolute_addr & 0x00FFFFFFu);
        snprintf(expr, expr_size, "%s", tmp);
        return 1;
    case NG_M68K_EA_AINDEX: {
        char index_expr[64];
        snprintf(tmp, sizeof(tmp), "ng_ea_%06X", instr->addr & 0x00FFFFFFu);
        ng_format_index_expr(ea, index_expr, (unsigned)sizeof(index_expr));
        fprintf(out, "    %s %s = %s((uint32_t)(g_ng_m68k.a[%u] + %s + (int32_t)%d));\n",
                ctype, tmp, read_fn, ea->reg, index_expr, ea->displacement);
        snprintf(expr, expr_size, "%s", tmp);
        return 1;
    }
    case NG_M68K_EA_PC_INDEX: {
        char index_expr[64];
        snprintf(tmp, sizeof(tmp), "ng_ea_%06X", instr->addr & 0x00FFFFFFu);
        ng_format_index_expr(ea, index_expr, (unsigned)sizeof(index_expr));
        fprintf(out, "    %s %s = %s((uint32_t)(0x%08Xu + %s));\n",
                ctype, tmp, read_fn, ea->absolute_addr & 0x00FFFFFFu, index_expr);
        snprintf(expr, expr_size, "%s", tmp);
        return 1;
    }
    case NG_M68K_EA_IMM:
        snprintf(expr, expr_size, "0x%0*Xu",
                 size == 4u ? 8 : (size == 1u ? 2 : 4),
                 ea->immediate & ng_value_mask(size));
        return 1;
    case NG_M68K_EA_NONE:
    default:
        return 0;
    }
}

static int emit_ea_write(FILE *out,
                         const NgM68kEa *ea,
                         uint8_t size,
                         const char *expr) {
    const char *write_fn = ng_write_fn_for_size(size);
    const char *ctype = ng_ctype_for_size(size);
    uint32_t keep_mask = ng_dreg_keep_mask(size);

    switch (ea->mode) {
    case NG_M68K_EA_DREG:
        if (size == 4u) {
            fprintf(out, "    g_ng_m68k.d[%u] = (uint32_t)(%s);\n", ea->reg, expr);
        } else {
            fprintf(out,
                    "    g_ng_m68k.d[%u] = (g_ng_m68k.d[%u] & 0x%08Xu) | (uint32_t)((%s)(%s));\n",
                    ea->reg, ea->reg, keep_mask, ctype, expr);
        }
        return 1;
    case NG_M68K_EA_AREG:
        fprintf(out, "    g_ng_m68k.a[%u] = (uint32_t)(%s);\n", ea->reg, expr);
        return 1;
    case NG_M68K_EA_AIND:
        fprintf(out, "    %s(g_ng_m68k.a[%u], (%s)(%s));\n",
                write_fn, ea->reg, ctype, expr);
        return 1;
    case NG_M68K_EA_APOST:
        fprintf(out, "    %s(g_ng_m68k.a[%u], (%s)(%s));\n",
                write_fn, ea->reg, ctype, expr);
        fprintf(out, "    g_ng_m68k.a[%u] += %uu;\n",
                ea->reg, (unsigned)ng_ea_step_bytes(ea, size));
        return 1;
    case NG_M68K_EA_APRE:
        fprintf(out, "    g_ng_m68k.a[%u] -= %uu;\n",
                ea->reg, (unsigned)ng_ea_step_bytes(ea, size));
        fprintf(out, "    %s(g_ng_m68k.a[%u], (%s)(%s));\n",
                write_fn, ea->reg, ctype, expr);
        return 1;
    case NG_M68K_EA_ADISP:
        fprintf(out, "    %s((uint32_t)(g_ng_m68k.a[%u] + (int32_t)%d), (%s)(%s));\n",
                write_fn, ea->reg, ea->displacement, ctype, expr);
        return 1;
    case NG_M68K_EA_ABS_W:
    case NG_M68K_EA_ABS_L:
        fprintf(out, "    %s(0x%08Xu, (%s)(%s));\n",
                write_fn, ea->absolute_addr & 0x00FFFFFFu, ctype, expr);
        return 1;
    case NG_M68K_EA_AINDEX: {
        char index_expr[64];
        ng_format_index_expr(ea, index_expr, (unsigned)sizeof(index_expr));
        fprintf(out,
                "    %s((uint32_t)(g_ng_m68k.a[%u] + %s + (int32_t)%d), (%s)(%s));\n",
                write_fn, ea->reg, index_expr, ea->displacement, ctype, expr);
        return 1;
    }
    case NG_M68K_EA_NONE:
    case NG_M68K_EA_PC_DISP:
    case NG_M68K_EA_PC_INDEX:
    case NG_M68K_EA_IMM:
    default:
        return 0;
    }
}

static int emit_ea_address(FILE *out,
                           const NgM68kInstr *instr,
                           const NgM68kEa *ea,
                           uint8_t size,
                           char *expr,
                           unsigned expr_size) {
    char tmp[48];

    switch (ea->mode) {
    case NG_M68K_EA_AIND:
        snprintf(expr, expr_size, "g_ng_m68k.a[%u]", ea->reg);
        return 1;
    case NG_M68K_EA_APOST:
        snprintf(tmp, sizeof(tmp), "ng_addr_%06X", instr->addr & 0x00FFFFFFu);
        fprintf(out, "    uint32_t %s = g_ng_m68k.a[%u];\n", tmp, ea->reg);
        fprintf(out, "    g_ng_m68k.a[%u] += %uu;\n",
                ea->reg, (unsigned)ng_ea_step_bytes(ea, size));
        snprintf(expr, expr_size, "%s", tmp);
        return 1;
    case NG_M68K_EA_APRE:
        fprintf(out, "    g_ng_m68k.a[%u] -= %uu;\n",
                ea->reg, (unsigned)ng_ea_step_bytes(ea, size));
        snprintf(expr, expr_size, "g_ng_m68k.a[%u]", ea->reg);
        return 1;
    case NG_M68K_EA_ADISP:
        snprintf(expr, expr_size, "(uint32_t)(g_ng_m68k.a[%u] + (int32_t)%d)",
                 ea->reg, ea->displacement);
        return 1;
    case NG_M68K_EA_ABS_W:
    case NG_M68K_EA_ABS_L:
        snprintf(expr, expr_size, "0x%08Xu", ea->absolute_addr & 0x00FFFFFFu);
        return 1;
    case NG_M68K_EA_AINDEX: {
        char index_expr[64];
        ng_format_index_expr(ea, index_expr, (unsigned)sizeof(index_expr));
        snprintf(expr, expr_size, "(uint32_t)(g_ng_m68k.a[%u] + %s + (int32_t)%d)",
                 ea->reg, index_expr, ea->displacement);
        return 1;
    }
    case NG_M68K_EA_NONE:
    case NG_M68K_EA_DREG:
    case NG_M68K_EA_AREG:
    case NG_M68K_EA_PC_DISP:
    case NG_M68K_EA_PC_INDEX:
    case NG_M68K_EA_IMM:
    default:
        return 0;
    }
}

static int emit_ea_address_value(const NgM68kEa *ea,
                                 char *expr,
                                 unsigned expr_size) {
    switch (ea->mode) {
    case NG_M68K_EA_AIND:
        snprintf(expr, expr_size, "g_ng_m68k.a[%u]", ea->reg);
        return 1;
    case NG_M68K_EA_ADISP:
        snprintf(expr, expr_size, "(uint32_t)(g_ng_m68k.a[%u] + (int32_t)%d)",
                 ea->reg, ea->displacement);
        return 1;
    case NG_M68K_EA_AINDEX: {
        char index_expr[64];
        ng_format_index_expr(ea, index_expr, (unsigned)sizeof(index_expr));
        snprintf(expr, expr_size, "(uint32_t)(g_ng_m68k.a[%u] + %s + (int32_t)%d)",
                 ea->reg, index_expr, ea->displacement);
        return 1;
    }
    case NG_M68K_EA_ABS_W:
    case NG_M68K_EA_ABS_L:
    case NG_M68K_EA_PC_DISP:
        snprintf(expr, expr_size, "0x%08Xu", ea->absolute_addr & 0x00FFFFFFu);
        return 1;
    case NG_M68K_EA_PC_INDEX: {
        char index_expr[64];
        ng_format_index_expr(ea, index_expr, (unsigned)sizeof(index_expr));
        snprintf(expr, expr_size, "(uint32_t)(0x%08Xu + %s)",
                 ea->absolute_addr & 0x00FFFFFFu, index_expr);
        return 1;
    }
    case NG_M68K_EA_NONE:
    case NG_M68K_EA_DREG:
    case NG_M68K_EA_AREG:
    case NG_M68K_EA_APOST:
    case NG_M68K_EA_APRE:
    case NG_M68K_EA_IMM:
    default:
        return 0;
    }
}

static void emit_set_nz_for_size(FILE *out, uint8_t size, const char *expr) {
    if (size == 4u) {
        fprintf(out, "    ng_set_nz32((uint32_t)(%s));\n", expr);
    } else if (size == 1u) {
        fprintf(out, "    ng_set_nz8((uint8_t)(%s));\n", expr);
    } else {
        fprintf(out, "    ng_set_nz16((uint16_t)(%s));\n", expr);
    }
}

static int emit_move_generic(FILE *out, const NgM68kInstr *instr) {
    char expr[256];
    if (instr->src.mode == NG_M68K_EA_NONE || instr->dst.mode == NG_M68K_EA_NONE) {
        return 0;
    }
    if (!emit_ea_read(out, instr, &instr->src, instr->size, expr, (unsigned)sizeof(expr))) {
        return 0;
    }
    if (!emit_ea_write(out, &instr->dst, instr->size, expr)) {
        return 0;
    }
    emit_set_nz_for_size(out, instr->size, expr);
    return 1;
}

static int emit_movea_generic(FILE *out, const NgM68kInstr *instr) {
    char expr[256];
    if (instr->src.mode == NG_M68K_EA_NONE || instr->dst.mode != NG_M68K_EA_AREG) {
        return 0;
    }
    if (!emit_ea_read(out, instr, &instr->src, instr->size, expr, (unsigned)sizeof(expr))) {
        return 0;
    }
    if (instr->size == 2u) {
        fprintf(out, "    g_ng_m68k.a[%u] = (uint32_t)(int32_t)(int16_t)(%s);\n",
                instr->dst.reg, expr);
    } else {
        fprintf(out, "    g_ng_m68k.a[%u] = (uint32_t)(%s);\n", instr->dst.reg, expr);
    }
    return 1;
}

static int emit_movem(FILE *out, const NgM68kInstr *instr) {
    uint16_t mask = (uint16_t)(instr->immediate & 0xFFFFu);
    const char *read_fn = ng_read_fn_for_size(instr->size);
    const char *write_fn = ng_write_fn_for_size(instr->size);
    char addr_expr[256];

    if (instr->dst.mode != NG_M68K_EA_NONE) {
        if (instr->dst.mode == NG_M68K_EA_APRE) {
            fprintf(out, "    { uint32_t ng_addr = g_ng_m68k.a[%u];\n",
                    instr->dst.reg);
            for (uint8_t bit = 0; bit < 16u; ++bit) {
                uint8_t is_addr;
                uint8_t reg;
                if ((mask & (uint16_t)(1u << bit)) == 0) {
                    continue;
                }
                is_addr = bit < 8u;
                reg = is_addr ? (uint8_t)(7u - bit) : (uint8_t)(15u - bit);
                fprintf(out, "      ng_addr -= %uu;\n", (unsigned)instr->size);
                if (is_addr) {
                    fprintf(out, "      %s(ng_addr, (%s)(g_ng_m68k.a[%u]));\n",
                            write_fn, ng_ctype_for_size(instr->size), reg);
                } else {
                    fprintf(out, "      %s(ng_addr, (%s)(g_ng_m68k.d[%u]));\n",
                            write_fn, ng_ctype_for_size(instr->size), reg);
                }
            }
            fprintf(out, "      g_ng_m68k.a[%u] = ng_addr;\n", instr->dst.reg);
            fprintf(out, "    }\n");
            return 1;
        }
        if (!emit_ea_address_value(&instr->dst, addr_expr, (unsigned)sizeof(addr_expr))) {
            return 0;
        }
        fprintf(out, "    { uint32_t ng_addr = (uint32_t)(%s);\n", addr_expr);
        for (uint8_t i = 0; i < 16u; ++i) {
            if ((mask & (uint16_t)(1u << i)) == 0) {
                continue;
            }
            if (i < 8u) {
                fprintf(out, "      %s(ng_addr, (%s)(g_ng_m68k.d[%u])); ng_addr += %uu;\n",
                        write_fn, ng_ctype_for_size(instr->size), i, (unsigned)instr->size);
            } else {
                fprintf(out, "      %s(ng_addr, (%s)(g_ng_m68k.a[%u])); ng_addr += %uu;\n",
                        write_fn, ng_ctype_for_size(instr->size), i - 8u, (unsigned)instr->size);
            }
        }
        fprintf(out, "    }\n");
        return 1;
    }

    if (instr->src.mode != NG_M68K_EA_NONE) {
        int postincrement = instr->src.mode == NG_M68K_EA_APOST;
        if (postincrement) {
            snprintf(addr_expr, sizeof(addr_expr), "g_ng_m68k.a[%u]", instr->src.reg);
        } else if (!emit_ea_address_value(&instr->src, addr_expr, (unsigned)sizeof(addr_expr))) {
            return 0;
        }
        fprintf(out, "    { uint32_t ng_addr = (uint32_t)(%s);\n", addr_expr);
        for (uint8_t i = 0; i < 16u; ++i) {
            if ((mask & (uint16_t)(1u << i)) == 0) {
                continue;
            }
            if (i < 8u) {
                if (instr->size == 4u) {
                    fprintf(out, "      g_ng_m68k.d[%u] = %s(ng_addr); ng_addr += %uu;\n",
                            i, read_fn, (unsigned)instr->size);
                } else {
                    fprintf(out, "      g_ng_m68k.d[%u] = (uint32_t)(int32_t)(int16_t)%s(ng_addr); ng_addr += %uu;\n",
                            i, read_fn, (unsigned)instr->size);
                }
            } else {
                if (instr->size == 4u) {
                    fprintf(out, "      g_ng_m68k.a[%u] = %s(ng_addr); ng_addr += %uu;\n",
                            i - 8u, read_fn, (unsigned)instr->size);
                } else {
                    fprintf(out, "      g_ng_m68k.a[%u] = (uint32_t)(int32_t)(int16_t)%s(ng_addr); ng_addr += %uu;\n",
                            i - 8u, read_fn, (unsigned)instr->size);
                }
            }
        }
        if (postincrement) {
            fprintf(out, "      g_ng_m68k.a[%u] = ng_addr;\n", instr->src.reg);
        }
        fprintf(out, "    }\n");
        return 1;
    }

    return 0;
}

static int emit_movep(FILE *out, const NgM68kInstr *instr) {
    const NgM68kEa *mem;
    uint8_t reg;

    if (instr->mnemonic != NG_M68K_MOVEP) {
        return 0;
    }

    if (instr->src.mode == NG_M68K_EA_DREG &&
        instr->dst.mode == NG_M68K_EA_ADISP) {
        mem = &instr->dst;
        reg = instr->src.reg;
        fprintf(out, "    { uint32_t ng_addr = (uint32_t)(g_ng_m68k.a[%u] + (int32_t)%d); uint32_t ng_value = g_ng_m68k.d[%u];\n",
                mem->reg, mem->displacement, reg);
        if (instr->size == 4u) {
            fprintf(out, "      ng68k_write8(ng_addr, (uint8_t)(ng_value >> 24)); ng68k_write8(ng_addr + 2u, (uint8_t)(ng_value >> 16)); ng68k_write8(ng_addr + 4u, (uint8_t)(ng_value >> 8)); ng68k_write8(ng_addr + 6u, (uint8_t)ng_value);\n");
        } else {
            fprintf(out, "      ng68k_write8(ng_addr, (uint8_t)(ng_value >> 8)); ng68k_write8(ng_addr + 2u, (uint8_t)ng_value);\n");
        }
        fprintf(out, "    }\n");
        return 1;
    }

    if (instr->src.mode == NG_M68K_EA_ADISP &&
        instr->dst.mode == NG_M68K_EA_DREG) {
        mem = &instr->src;
        reg = instr->dst.reg;
        fprintf(out, "    { uint32_t ng_addr = (uint32_t)(g_ng_m68k.a[%u] + (int32_t)%d);\n",
                mem->reg, mem->displacement);
        if (instr->size == 4u) {
            fprintf(out, "      g_ng_m68k.d[%u] = ((uint32_t)ng68k_read8(ng_addr) << 24) | ((uint32_t)ng68k_read8(ng_addr + 2u) << 16) | ((uint32_t)ng68k_read8(ng_addr + 4u) << 8) | (uint32_t)ng68k_read8(ng_addr + 6u);\n",
                    reg);
        } else {
            fprintf(out, "      g_ng_m68k.d[%u] = (g_ng_m68k.d[%u] & 0xFFFF0000u) | ((uint32_t)ng68k_read8(ng_addr) << 8) | (uint32_t)ng68k_read8(ng_addr + 2u);\n",
                    reg, reg);
        }
        fprintf(out, "    }\n");
        return 1;
    }

    return 0;
}

static int emit_move_sr_ccr(FILE *out, const NgM68kInstr *instr) {
    char expr[256];

    if (instr->mnemonic != NG_M68K_MOVE_SR &&
        instr->mnemonic != NG_M68K_MOVE_CCR) {
        return 0;
    }

    if (instr->dst.mode != NG_M68K_EA_NONE) {
        const char *value = instr->mnemonic == NG_M68K_MOVE_SR ?
            "g_ng_m68k.sr" : "(g_ng_m68k.sr & 0x001Fu)";
        return emit_ea_write(out, &instr->dst, 2u, value);
    }

    if (instr->src.mode != NG_M68K_EA_NONE) {
        if (instr->mnemonic == NG_M68K_MOVE_SR) {
            emit_require_supervisor(out, instr->addr);
        }
        if (!emit_ea_read(out, instr, &instr->src, 2u, expr, (unsigned)sizeof(expr))) {
            return 0;
        }
        if (instr->mnemonic == NG_M68K_MOVE_SR) {
            fprintf(out, "    ng_set_sr((uint16_t)(%s));\n", expr);
        } else {
            fprintf(out, "    g_ng_m68k.sr = (uint16_t)((g_ng_m68k.sr & 0xFFE0u) | ((%s) & 0x001Fu));\n",
                    expr);
        }
        return 1;
    }

    return 0;
}

static int emit_immediate_to_sr_ccr(FILE *out, const NgM68kInstr *instr) {
    uint32_t imm = instr->immediate;

    switch (instr->mnemonic) {
    case NG_M68K_ORI_TO_CCR:
        fprintf(out,
                "    g_ng_m68k.sr = (uint16_t)((g_ng_m68k.sr & 0xFFE0u) | ((g_ng_m68k.sr | 0x%02Xu) & 0x001Fu));\n",
                imm & 0xFFu);
        return 1;
    case NG_M68K_ANDI_TO_CCR:
        fprintf(out,
                "    g_ng_m68k.sr = (uint16_t)((g_ng_m68k.sr & 0xFFE0u) | ((g_ng_m68k.sr & 0x%02Xu) & 0x001Fu));\n",
                imm & 0xFFu);
        return 1;
    case NG_M68K_EORI_TO_CCR:
        fprintf(out,
                "    g_ng_m68k.sr = (uint16_t)((g_ng_m68k.sr & 0xFFE0u) | ((g_ng_m68k.sr ^ 0x%02Xu) & 0x001Fu));\n",
                imm & 0xFFu);
        return 1;
    case NG_M68K_ORI_TO_SR:
        emit_require_supervisor(out, instr->addr);
        fprintf(out, "    ng_set_sr((uint16_t)(g_ng_m68k.sr | 0x%04Xu));\n",
                imm & 0xFFFFu);
        return 1;
    case NG_M68K_ANDI_TO_SR:
        emit_require_supervisor(out, instr->addr);
        fprintf(out, "    ng_set_sr((uint16_t)(g_ng_m68k.sr & 0x%04Xu));\n",
                imm & 0xFFFFu);
        return 1;
    case NG_M68K_EORI_TO_SR:
        emit_require_supervisor(out, instr->addr);
        fprintf(out, "    ng_set_sr((uint16_t)(g_ng_m68k.sr ^ 0x%04Xu));\n",
                imm & 0xFFFFu);
        return 1;
    default:
        return 0;
    }
}

static int emit_tst_generic(FILE *out, const NgM68kInstr *instr) {
    char expr[256];
    if (instr->src.mode == NG_M68K_EA_NONE) {
        return 0;
    }
    if (!emit_ea_read(out, instr, &instr->src, instr->size, expr, (unsigned)sizeof(expr))) {
        return 0;
    }
    emit_set_nz_for_size(out, instr->size, expr);
    return 1;
}

static int emit_tas_generic(FILE *out, const NgM68kInstr *instr) {
    char addr_expr[256];

    if (instr->dst.mode == NG_M68K_EA_NONE || instr->size != 1u) {
        return 0;
    }

    if (instr->dst.mode == NG_M68K_EA_DREG) {
        fprintf(out, "    { uint8_t ng_value = (uint8_t)(g_ng_m68k.d[%u] & 0xFFu);\n",
                instr->dst.reg);
        fprintf(out, "      ng_set_nz8(ng_value);\n");
        fprintf(out, "      g_ng_m68k.d[%u] = (g_ng_m68k.d[%u] & 0xFFFFFF00u) | (uint32_t)(uint8_t)(ng_value | 0x80u);\n",
                instr->dst.reg, instr->dst.reg);
        fprintf(out, "    }\n");
        return 1;
    }

    if (!emit_ea_address(out, instr, &instr->dst, 1u,
                         addr_expr, (unsigned)sizeof(addr_expr))) {
        return 0;
    }
    fprintf(out, "    { uint32_t ng_addr = %s; uint8_t ng_value = ng68k_read8(ng_addr);\n",
            addr_expr);
    fprintf(out, "      ng_set_nz8(ng_value);\n");
    fprintf(out, "      ng68k_write8(ng_addr, (uint8_t)(ng_value | 0x80u));\n");
    fprintf(out, "    }\n");
    return 1;
}

static void emit_nbcd_core(FILE *out, const char *value_expr, const char *write_stmt) {
    fprintf(out, "    { uint8_t ng_value = (uint8_t)(%s); uint8_t ng_x = (g_ng_m68k.sr & NG_CCR_X) ? 1u : 0u;\n",
            value_expr);
    fprintf(out, "      uint8_t ng_decimal = (uint8_t)(((ng_value >> 4) & 0x0Fu) * 10u + (ng_value & 0x0Fu));\n");
    fprintf(out, "      uint8_t ng_borrow = (uint8_t)((ng_decimal + ng_x) != 0u);\n");
    fprintf(out, "      uint8_t ng_result_decimal = (uint8_t)((100u - ng_decimal - ng_x) %% 100u);\n");
    fprintf(out, "      uint8_t ng_result = (uint8_t)(((ng_result_decimal / 10u) << 4) | (ng_result_decimal %% 10u));\n");
    fprintf(out, "      %s\n", write_stmt);
    fprintf(out, "      uint16_t ng_sr = (uint16_t)(g_ng_m68k.sr & 0xFFE4u);\n");
    fprintf(out, "      if (ng_result != 0) ng_sr = (uint16_t)(ng_sr & (uint16_t)~NG_CCR_Z);\n");
    fprintf(out, "      if (ng_result & 0x80u) ng_sr |= NG_CCR_N;\n");
    fprintf(out, "      if (ng_borrow) ng_sr |= NG_CCR_C | NG_CCR_X;\n");
    fprintf(out, "      g_ng_m68k.sr = ng_sr;\n");
    fprintf(out, "    }\n");
}

static int emit_nbcd_generic(FILE *out, const NgM68kInstr *instr) {
    char addr_expr[256];
    char value_expr[128];
    char write_stmt[256];

    if (instr->mnemonic != NG_M68K_NBCD ||
        instr->dst.mode == NG_M68K_EA_NONE ||
        instr->size != 1u) {
        return 0;
    }

    if (instr->dst.mode == NG_M68K_EA_DREG) {
        snprintf(value_expr, sizeof(value_expr), "g_ng_m68k.d[%u] & 0xFFu",
                 instr->dst.reg);
        snprintf(write_stmt, sizeof(write_stmt),
                 "g_ng_m68k.d[%u] = (g_ng_m68k.d[%u] & 0xFFFFFF00u) | (uint32_t)ng_result;",
                 instr->dst.reg, instr->dst.reg);
        emit_nbcd_core(out, value_expr, write_stmt);
        return 1;
    }

    if (!emit_ea_address(out, instr, &instr->dst, 1u,
                         addr_expr, (unsigned)sizeof(addr_expr))) {
        return 0;
    }
    snprintf(value_expr, sizeof(value_expr), "ng68k_read8(%s)", addr_expr);
    snprintf(write_stmt, sizeof(write_stmt), "ng68k_write8(%s, ng_result);",
             addr_expr);
    emit_nbcd_core(out, value_expr, write_stmt);
    return 1;
}

static uint32_t ng_sign_mask(uint8_t size) {
    if (size == 4u) {
        return 0x80000000u;
    }
    return size == 1u ? 0x00000080u : 0x00008000u;
}

static int ng_size_bits(uint8_t size) {
    return size == 4u ? 32 : (size == 1u ? 8 : 16);
}

static int emit_cmpi_generic(FILE *out, const NgM68kInstr *instr) {
    char dst_expr[256];
    const char *ctype = ng_ctype_for_size(instr->size);
    uint32_t sign_mask = ng_sign_mask(instr->size);

    if (instr->dst.mode == NG_M68K_EA_NONE) {
        return 0;
    }
    if (!emit_ea_read(out, instr, &instr->dst, instr->size,
                      dst_expr, (unsigned)sizeof(dst_expr))) {
        return 0;
    }

    fprintf(out,
            "    { %s ng_src = (%s)(0x%0*Xu); %s ng_dst = (%s)(%s); %s ng_result = (%s)(ng_dst - ng_src);\n",
            ctype, ctype,
            instr->size == 4u ? 8 : (instr->size == 1u ? 2 : 4),
            instr->immediate & ng_value_mask(instr->size),
            ctype, ctype, dst_expr,
            ctype, ctype);
    fprintf(out,
            "      g_ng_m68k.sr = (uint16_t)(g_ng_m68k.sr & 0xFFF0u);\n"
            "      if (ng_result == 0) g_ng_m68k.sr |= NG_CCR_Z;\n"
            "      if (ng_result & 0x%08Xu) g_ng_m68k.sr |= NG_CCR_N;\n"
            "      if (ng_src > ng_dst) g_ng_m68k.sr |= NG_CCR_C;\n"
            "      if (((ng_dst ^ ng_src) & (ng_dst ^ ng_result) & 0x%08Xu) != 0) g_ng_m68k.sr |= NG_CCR_V;\n"
            "    }\n",
            sign_mask, sign_mask);
    return 1;
}

static int emit_cmpm(FILE *out, const NgM68kInstr *instr) {
    const char *ctype = ng_ctype_for_size(instr->size);
    const char *read_fn = ng_read_fn_for_size(instr->size);
    uint32_t value_mask = ng_value_mask(instr->size);
    uint32_t sign_mask = ng_sign_mask(instr->size);
    uint8_t step = instr->size;

    if (instr->mnemonic != NG_M68K_CMPM ||
        instr->src.mode != NG_M68K_EA_APOST ||
        instr->dst.mode != NG_M68K_EA_APOST) {
        return 0;
    }
    if (instr->src.reg == 7u && instr->size == 1u) {
        step = 2u;
    }

    fprintf(out, "    { uint32_t ng_src_addr = g_ng_m68k.a[%u]; g_ng_m68k.a[%u] += %uu;\n",
            instr->src.reg, instr->src.reg, (unsigned)step);
    step = instr->size;
    if (instr->dst.reg == 7u && instr->size == 1u) {
        step = 2u;
    }
    fprintf(out, "      uint32_t ng_dst_addr = g_ng_m68k.a[%u]; g_ng_m68k.a[%u] += %uu;\n",
            instr->dst.reg, instr->dst.reg, (unsigned)step);
    fprintf(out, "      %s ng_src = %s(ng_src_addr); %s ng_dst = %s(ng_dst_addr); %s ng_result = (%s)(ng_dst - ng_src);\n",
            ctype, read_fn, ctype, read_fn, ctype, ctype);
    fprintf(out, "      g_ng_m68k.sr = (uint16_t)(g_ng_m68k.sr & 0xFFF0u);\n");
    fprintf(out, "      if (ng_result == 0) g_ng_m68k.sr |= NG_CCR_Z;\n");
    fprintf(out, "      if (ng_result & 0x%08Xu) g_ng_m68k.sr |= NG_CCR_N;\n",
            sign_mask);
    fprintf(out, "      if ((uint32_t)ng_src > (uint32_t)ng_dst) g_ng_m68k.sr |= NG_CCR_C;\n");
    fprintf(out, "      if ((((uint32_t)ng_dst ^ (uint32_t)ng_src) & ((uint32_t)ng_dst ^ (uint32_t)ng_result) & 0x%08Xu) != 0) g_ng_m68k.sr |= NG_CCR_V;\n",
            sign_mask);
    fprintf(out, "    }\n");
    (void)value_mask;
    return 1;
}

static int emit_addsubx(FILE *out, const NgM68kInstr *instr) {
    const char *ctype = ng_ctype_for_size(instr->size);
    const char *read_fn = ng_read_fn_for_size(instr->size);
    const char *write_fn = ng_write_fn_for_size(instr->size);
    uint32_t keep_mask = ng_dreg_keep_mask(instr->size);
    uint32_t value_mask = ng_value_mask(instr->size);
    uint32_t sign_mask = ng_sign_mask(instr->size);
    int is_sub = instr->mnemonic == NG_M68K_SUBX;

    if (instr->mnemonic != NG_M68K_ADDX &&
        instr->mnemonic != NG_M68K_SUBX) {
        return 0;
    }

    if (instr->src.mode == NG_M68K_EA_DREG &&
        instr->dst.mode == NG_M68K_EA_DREG) {
        fprintf(out,
                "    { %s ng_src = (%s)(g_ng_m68k.d[%u] & 0x%08Xu); %s ng_dst = (%s)(g_ng_m68k.d[%u] & 0x%08Xu); uint8_t ng_x = (g_ng_m68k.sr & NG_CCR_X) ? 1u : 0u;\n",
                ctype, ctype, instr->src.reg, value_mask,
                ctype, ctype, instr->dst.reg, value_mask);
    } else if (instr->src.mode == NG_M68K_EA_APRE &&
               instr->dst.mode == NG_M68K_EA_APRE) {
        uint8_t src_step = ng_ea_step_bytes(&instr->src, instr->size);
        uint8_t dst_step = ng_ea_step_bytes(&instr->dst, instr->size);
        fprintf(out,
                "    { g_ng_m68k.a[%u] -= %uu; uint32_t ng_src_addr = g_ng_m68k.a[%u]; g_ng_m68k.a[%u] -= %uu; uint32_t ng_dst_addr = g_ng_m68k.a[%u];\n",
                instr->src.reg, (unsigned)src_step, instr->src.reg,
                instr->dst.reg, (unsigned)dst_step, instr->dst.reg);
        fprintf(out,
                "      %s ng_src = %s(ng_src_addr); %s ng_dst = %s(ng_dst_addr); uint8_t ng_x = (g_ng_m68k.sr & NG_CCR_X) ? 1u : 0u;\n",
                ctype, read_fn, ctype, read_fn);
    } else {
        return 0;
    }

    if (is_sub) {
        fprintf(out,
                "      uint64_t ng_src_full = (uint64_t)ng_src + (uint64_t)ng_x; %s ng_src_v = (%s)(ng_src_full & 0x%08Xu); %s ng_result = (%s)((uint64_t)ng_dst - ng_src_full);\n",
                ctype, ctype, value_mask, ctype, ctype);
    } else {
        fprintf(out,
                "      uint64_t ng_full = (uint64_t)ng_dst + (uint64_t)ng_src + (uint64_t)ng_x; %s ng_result = (%s)ng_full;\n",
                ctype, ctype);
    }

    if (instr->dst.mode == NG_M68K_EA_DREG) {
        fprintf(out, "      g_ng_m68k.d[%u] = (g_ng_m68k.d[%u] & 0x%08Xu) | (uint32_t)(ng_result & 0x%08Xu);\n",
                instr->dst.reg, instr->dst.reg, keep_mask, value_mask);
    } else {
        fprintf(out, "      %s(ng_dst_addr, ng_result);\n", write_fn);
    }

    fprintf(out, "      uint16_t ng_sr = (uint16_t)(g_ng_m68k.sr & 0xFFE4u);\n");
    fprintf(out, "      if (ng_result != 0) ng_sr = (uint16_t)(ng_sr & (uint16_t)~NG_CCR_Z);\n");
    fprintf(out, "      if (ng_result & 0x%08Xu) ng_sr |= NG_CCR_N;\n", sign_mask);
    if (is_sub) {
        fprintf(out, "      if (ng_src_full > (uint64_t)ng_dst) ng_sr |= NG_CCR_C | NG_CCR_X;\n");
        fprintf(out, "      if ((((uint32_t)ng_dst ^ (uint32_t)ng_src_v) & ((uint32_t)ng_dst ^ (uint32_t)ng_result) & 0x%08Xu) != 0) ng_sr |= NG_CCR_V;\n",
                sign_mask);
    } else {
        fprintf(out, "      if (ng_full > 0x%08Xu) ng_sr |= NG_CCR_C | NG_CCR_X;\n",
                value_mask);
        fprintf(out, "      if (((~((uint32_t)ng_dst ^ (uint32_t)ng_src) & ((uint32_t)ng_dst ^ (uint32_t)ng_result)) & 0x%08Xu) != 0) ng_sr |= NG_CCR_V;\n",
                sign_mask);
    }
    fprintf(out, "      g_ng_m68k.sr = ng_sr;\n");
    fprintf(out, "    }\n");
    return 1;
}

static int emit_abcd_sbcd(FILE *out, const NgM68kInstr *instr) {
    int is_sub = instr->mnemonic == NG_M68K_SBCD;

    if (instr->mnemonic != NG_M68K_ABCD &&
        instr->mnemonic != NG_M68K_SBCD) {
        return 0;
    }

    if (instr->src.mode == NG_M68K_EA_DREG &&
        instr->dst.mode == NG_M68K_EA_DREG) {
        fprintf(out,
                "    { uint8_t ng_src = (uint8_t)(g_ng_m68k.d[%u] & 0xFFu); uint8_t ng_dst = (uint8_t)(g_ng_m68k.d[%u] & 0xFFu); uint8_t ng_x = (g_ng_m68k.sr & NG_CCR_X) ? 1u : 0u;\n",
                instr->src.reg, instr->dst.reg);
    } else if (instr->src.mode == NG_M68K_EA_APRE &&
               instr->dst.mode == NG_M68K_EA_APRE) {
        uint8_t src_step = ng_ea_step_bytes(&instr->src, 1u);
        uint8_t dst_step = ng_ea_step_bytes(&instr->dst, 1u);
        fprintf(out,
                "    { g_ng_m68k.a[%u] -= %uu; uint32_t ng_src_addr = g_ng_m68k.a[%u]; g_ng_m68k.a[%u] -= %uu; uint32_t ng_dst_addr = g_ng_m68k.a[%u];\n",
                instr->src.reg, (unsigned)src_step, instr->src.reg,
                instr->dst.reg, (unsigned)dst_step, instr->dst.reg);
        fprintf(out,
                "      uint8_t ng_src = ng68k_read8(ng_src_addr); uint8_t ng_dst = ng68k_read8(ng_dst_addr); uint8_t ng_x = (g_ng_m68k.sr & NG_CCR_X) ? 1u : 0u;\n");
    } else {
        return 0;
    }

    fprintf(out, "      uint8_t ng_src_dec = (uint8_t)(((ng_src >> 4) & 0x0Fu) * 10u + (ng_src & 0x0Fu));\n");
    fprintf(out, "      uint8_t ng_dst_dec = (uint8_t)(((ng_dst >> 4) & 0x0Fu) * 10u + (ng_dst & 0x0Fu));\n");
    if (is_sub) {
        fprintf(out, "      uint16_t ng_src_full = (uint16_t)ng_src_dec + (uint16_t)ng_x; uint8_t ng_borrow = (uint8_t)(ng_src_full > ng_dst_dec); uint8_t ng_result_dec = (uint8_t)((100u + ng_dst_dec - ng_src_full) %% 100u);\n");
    } else {
        fprintf(out, "      uint16_t ng_sum = (uint16_t)ng_dst_dec + (uint16_t)ng_src_dec + (uint16_t)ng_x; uint8_t ng_borrow = (uint8_t)(ng_sum > 99u); uint8_t ng_result_dec = (uint8_t)(ng_sum %% 100u);\n");
    }
    fprintf(out, "      uint8_t ng_result = (uint8_t)(((ng_result_dec / 10u) << 4) | (ng_result_dec %% 10u));\n");
    if (instr->dst.mode == NG_M68K_EA_DREG) {
        fprintf(out, "      g_ng_m68k.d[%u] = (g_ng_m68k.d[%u] & 0xFFFFFF00u) | (uint32_t)ng_result;\n",
                instr->dst.reg, instr->dst.reg);
    } else {
        fprintf(out, "      ng68k_write8(ng_dst_addr, ng_result);\n");
    }
    fprintf(out, "      uint16_t ng_sr = (uint16_t)(g_ng_m68k.sr & 0xFFE4u);\n");
    fprintf(out, "      if (ng_result != 0) ng_sr = (uint16_t)(ng_sr & (uint16_t)~NG_CCR_Z);\n");
    fprintf(out, "      if (ng_result & 0x80u) ng_sr |= NG_CCR_N;\n");
    fprintf(out, "      if (ng_borrow) ng_sr |= NG_CCR_C | NG_CCR_X;\n");
    fprintf(out, "      g_ng_m68k.sr = ng_sr;\n");
    fprintf(out, "    }\n");
    return 1;
}

static int emit_chk(FILE *out, const NgM68kInstr *instr) {
    char src_expr[256];

    if (instr->mnemonic != NG_M68K_CHK ||
        instr->src.mode == NG_M68K_EA_NONE ||
        instr->dst.mode != NG_M68K_EA_DREG ||
        instr->size != 2u) {
        return 0;
    }
    if (!emit_ea_read(out, instr, &instr->src, 2u,
                      src_expr, (unsigned)sizeof(src_expr))) {
        return 0;
    }

    fprintf(out, "    { int16_t ng_bound = (int16_t)(%s); int16_t ng_value = (int16_t)(g_ng_m68k.d[%u] & 0xFFFFu);\n",
            src_expr, instr->dst.reg);
    fprintf(out, "      if (ng_value < 0 || ng_value > ng_bound) {\n");
    fprintf(out, "        if (ng_value < 0) g_ng_m68k.sr |= NG_CCR_N; else g_ng_m68k.sr = (uint16_t)(g_ng_m68k.sr & (uint16_t)~NG_CCR_N);\n");
    fprintf(out, "        g_ng_m68k.a[7] -= 4u;\n");
    fprintf(out, "        ng68k_write32(g_ng_m68k.a[7], 0x%08Xu);\n",
            (instr->addr + instr->byte_length) & 0x00FFFFFFu);
    fprintf(out, "        g_ng_m68k.a[7] -= 2u;\n");
    fprintf(out, "        ng68k_write16(g_ng_m68k.a[7], g_ng_m68k.sr);\n");
    fprintf(out, "        ng_generated_call(ng68k_read32(0x00000018u));\n");
    fprintf(out, "        return;\n");
    fprintf(out, "      }\n");
    fprintf(out, "    }\n");
    return 1;
}

static int emit_logical_imm_generic(FILE *out, const NgM68kInstr *instr) {
    const char *ctype = ng_ctype_for_size(instr->size);
    const char *read_fn = ng_read_fn_for_size(instr->size);
    const char *write_fn = ng_write_fn_for_size(instr->size);
    const char *op = instr->mnemonic == NG_M68K_ORI ? "|" :
                     (instr->mnemonic == NG_M68K_EORI ? "^" : "&");
    uint32_t mask = ng_value_mask(instr->size);
    char expr[256];
    char addr_expr[256];

    if (instr->dst.mode == NG_M68K_EA_NONE) {
        return 0;
    }

    if (instr->dst.mode == NG_M68K_EA_DREG) {
        if (!emit_ea_read(out, instr, &instr->dst, instr->size,
                          expr, (unsigned)sizeof(expr))) {
            return 0;
        }
        fprintf(out, "    { %s ng_result = (%s)((%s) %s 0x%0*Xu);\n",
                ctype, ctype, expr, op,
                instr->size == 4u ? 8 : (instr->size == 1u ? 2 : 4),
                instr->immediate & mask);
        emit_ea_write(out, &instr->dst, instr->size, "ng_result");
        emit_set_nz_for_size(out, instr->size, "ng_result");
        fprintf(out, "    }\n");
        return 1;
    }

    if (!emit_ea_address(out, instr, &instr->dst, instr->size,
                         addr_expr, (unsigned)sizeof(addr_expr))) {
        return 0;
    }
    fprintf(out,
            "    { %s ng_value = %s(%s); %s ng_result = (%s)(ng_value %s 0x%0*Xu);\n",
            ctype, read_fn, addr_expr,
            ctype, ctype, op,
            instr->size == 4u ? 8 : (instr->size == 1u ? 2 : 4),
            instr->immediate & mask);
    fprintf(out, "      %s(%s, ng_result);\n", write_fn, addr_expr);
    emit_set_nz_for_size(out, instr->size, "ng_result");
    fprintf(out, "    }\n");
    return 1;
}

static int emit_bit_imm_generic(FILE *out, const NgM68kInstr *instr) {
    char addr_expr[256];
    char bit_expr[64];

    if (instr->dst.mode == NG_M68K_EA_NONE) {
        return 0;
    }

    if (instr->src.mode == NG_M68K_EA_DREG) {
        snprintf(bit_expr, sizeof(bit_expr), "g_ng_m68k.d[%u] & %s",
                 instr->src.reg,
                 instr->dst.mode == NG_M68K_EA_DREG ? "31u" : "7u");
    } else {
        snprintf(bit_expr, sizeof(bit_expr), "%uu",
                 (unsigned)(instr->immediate &
                     (instr->dst.mode == NG_M68K_EA_DREG ? 31u : 7u)));
    }

    if (instr->dst.mode == NG_M68K_EA_DREG) {
        fprintf(out,
                "    { uint32_t ng_mask = (uint32_t)(1u << (%s)); uint32_t ng_value = g_ng_m68k.d[%u];\n",
                bit_expr, instr->dst.reg);
        fprintf(out,
                "      if ((ng_value & ng_mask) == 0) g_ng_m68k.sr |= NG_CCR_Z; else g_ng_m68k.sr = (uint16_t)(g_ng_m68k.sr & (uint16_t)~NG_CCR_Z);\n");
        if (instr->mnemonic == NG_M68K_BCHG) {
            fprintf(out, "      g_ng_m68k.d[%u] = ng_value ^ ng_mask;\n",
                    instr->dst.reg);
        } else if (instr->mnemonic == NG_M68K_BCLR) {
            fprintf(out, "      g_ng_m68k.d[%u] = ng_value & (uint32_t)~ng_mask;\n",
                    instr->dst.reg);
        } else if (instr->mnemonic == NG_M68K_BSET) {
            fprintf(out, "      g_ng_m68k.d[%u] = ng_value | ng_mask;\n",
                    instr->dst.reg);
        }
        fprintf(out, "    }\n");
        return 1;
    }

    if (!emit_ea_address(out, instr, &instr->dst, 1u,
                         addr_expr, (unsigned)sizeof(addr_expr))) {
        return 0;
    }
    fprintf(out,
            "    { uint8_t ng_mask = (uint8_t)(1u << (%s)); uint8_t ng_value = ng68k_read8(%s);\n",
            bit_expr, addr_expr);
    fprintf(out,
            "      if ((ng_value & ng_mask) == 0) g_ng_m68k.sr |= NG_CCR_Z; else g_ng_m68k.sr = (uint16_t)(g_ng_m68k.sr & (uint16_t)~NG_CCR_Z);\n");
    if (instr->mnemonic == NG_M68K_BCHG) {
        fprintf(out, "      ng68k_write8(%s, (uint8_t)(ng_value ^ ng_mask));\n",
                addr_expr);
    } else if (instr->mnemonic == NG_M68K_BCLR) {
        fprintf(out, "      ng68k_write8(%s, (uint8_t)(ng_value & (uint8_t)~ng_mask));\n",
                addr_expr);
    } else if (instr->mnemonic == NG_M68K_BSET) {
        fprintf(out, "      ng68k_write8(%s, (uint8_t)(ng_value | ng_mask));\n",
                addr_expr);
    }
    fprintf(out, "    }\n");
    return 1;
}

static int emit_quick_generic(FILE *out, const NgM68kInstr *instr) {
    const char *ctype = ng_ctype_for_size(instr->size);
    const char *read_fn = ng_read_fn_for_size(instr->size);
    const char *write_fn = ng_write_fn_for_size(instr->size);
    uint32_t value_mask = ng_value_mask(instr->size);
    uint32_t sign_mask = ng_sign_mask(instr->size);
    char dst_expr[256];
    char addr_expr[256];

    if (instr->dst.mode == NG_M68K_EA_NONE) {
        return 0;
    }

    if (instr->dst.mode == NG_M68K_EA_AREG) {
        if (instr->mnemonic == NG_M68K_ADDQ) {
            fprintf(out, "    g_ng_m68k.a[%u] += %uu;\n",
                    instr->dst.reg, (unsigned)instr->immediate);
        } else {
            fprintf(out, "    g_ng_m68k.a[%u] -= %uu;\n",
                    instr->dst.reg, (unsigned)instr->immediate);
        }
        return 1;
    }

    if (instr->dst.mode == NG_M68K_EA_DREG) {
        if (!emit_ea_read(out, instr, &instr->dst, instr->size,
                          dst_expr, (unsigned)sizeof(dst_expr))) {
            return 0;
        }
        if (instr->mnemonic == NG_M68K_ADDQ) {
            fprintf(out,
                    "    { %s ng_src = (%s)(%uu); %s ng_dst = (%s)(%s); uint64_t ng_full = (uint64_t)ng_dst + (uint64_t)ng_src; %s ng_result = (%s)ng_full;\n",
                    ctype, ctype, (unsigned)instr->immediate,
                    ctype, ctype, dst_expr,
                    ctype, ctype);
        } else {
            fprintf(out,
                    "    { %s ng_src = (%s)(%uu); %s ng_dst = (%s)(%s); %s ng_result = (%s)(ng_dst - ng_src);\n",
                    ctype, ctype, (unsigned)instr->immediate,
                    ctype, ctype, dst_expr,
                    ctype, ctype);
        }
        emit_ea_write(out, &instr->dst, instr->size, "ng_result");
    } else {
        if (!emit_ea_address(out, instr, &instr->dst, instr->size,
                             addr_expr, (unsigned)sizeof(addr_expr))) {
            return 0;
        }
        if (instr->mnemonic == NG_M68K_ADDQ) {
            fprintf(out,
                    "    { %s ng_src = (%s)(%uu); %s ng_dst = %s(%s); uint64_t ng_full = (uint64_t)ng_dst + (uint64_t)ng_src; %s ng_result = (%s)ng_full;\n",
                    ctype, ctype, (unsigned)instr->immediate,
                    ctype, read_fn, addr_expr,
                    ctype, ctype);
        } else {
            fprintf(out,
                    "    { %s ng_src = (%s)(%uu); %s ng_dst = %s(%s); %s ng_result = (%s)(ng_dst - ng_src);\n",
                    ctype, ctype, (unsigned)instr->immediate,
                    ctype, read_fn, addr_expr,
                    ctype, ctype);
        }
        fprintf(out, "      %s(%s, ng_result);\n", write_fn, addr_expr);
    }

    fprintf(out, "      g_ng_m68k.sr = (uint16_t)(g_ng_m68k.sr & 0xFFE0u);\n");
    fprintf(out, "      if (ng_result == 0) g_ng_m68k.sr |= NG_CCR_Z;\n");
    fprintf(out, "      if (ng_result & 0x%08Xu) g_ng_m68k.sr |= NG_CCR_N;\n",
            sign_mask);
    if (instr->mnemonic == NG_M68K_ADDQ) {
        fprintf(out, "      if (ng_full > 0x%08Xu) g_ng_m68k.sr |= NG_CCR_C | NG_CCR_X;\n",
                value_mask);
        fprintf(out, "      if (((~(ng_dst ^ ng_src) & (ng_dst ^ ng_result)) & 0x%08Xu) != 0) g_ng_m68k.sr |= NG_CCR_V;\n",
                sign_mask);
    } else {
        fprintf(out, "      if (ng_src > ng_dst) g_ng_m68k.sr |= NG_CCR_C | NG_CCR_X;\n");
        fprintf(out, "      if (((ng_dst ^ ng_src) & (ng_dst ^ ng_result) & 0x%08Xu) != 0) g_ng_m68k.sr |= NG_CCR_V;\n",
                sign_mask);
    }
    fprintf(out, "    }\n");
    return 1;
}

